#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	char	*ch;
	size_t	i;

	ch = (char *)b;
	i = 0;
	while (i < len)
	{
		ch[i] = (char)c;
		i++;
	}
	return (b);
}
